/*
 * Processes a Diku file; adding dbkey_base to its dbkeys.
 */


int highestdbkey( ) {
    ZONE *pZone;
    int hv = 0;

    for (pZone = zone_list;  pZone != NULL;  pZone = pZone->next ) {
        if ( pZone->udbkey > hv ) hv = pZone->udbkey;
    }

    return hv;
}

void load_diku( char *filename ) {
   FILE *fp;

   fp = fopen( filename, "r" );  
}
